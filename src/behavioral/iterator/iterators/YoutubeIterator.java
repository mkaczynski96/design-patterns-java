package behavioral.iterator.iterators;

import behavioral.iterator.profile.Profile;
import behavioral.iterator.social_networks.Youtube;

import java.util.ArrayList;
import java.util.List;

public class YoutubeIterator implements ProfileIterator {
    private Youtube youtube;
    private String type;
    private String email;
    private int currentPosition = 0;
    private List<String> emails = new ArrayList<>();
    private List<Profile> profiles = new ArrayList<>();

    public YoutubeIterator(Youtube youtube, String type, String email) {
        this.youtube = youtube;
        this.type = type;
        this.email = email;
    }

    private void lazyLoad() {
        if (emails.size() == 0) {
            List<String> profiles = youtube.requestProfileFriendsFromYoutube(this.email, this.type);
            for (String profile : profiles) {
                this.emails.add(profile);
                this.profiles.add(null);
            }
        }
    }

    @Override
    public boolean hasNext() {
        lazyLoad();
        return currentPosition < emails.size();
    }

    @Override
    public Profile getNext() {
        if (!hasNext()) {
            return null;
        }

        String friendEmail = emails.get(currentPosition);
        Profile friendProfile = profiles.get(currentPosition);
        if (friendProfile == null) {
            friendProfile = youtube.requestProfileFromYoutube(friendEmail);
            profiles.set(currentPosition, friendProfile);
        }
        currentPosition++;
        return friendProfile;
    }

    @Override
    public void reset() {
        currentPosition = 0;
    }
}
